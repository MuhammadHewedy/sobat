package com.googlecode.sobat.examples;

public class Employee {
	private long id;
	private String name;
	
	public long getId() {
		return id;
	}
	private void setId(long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
